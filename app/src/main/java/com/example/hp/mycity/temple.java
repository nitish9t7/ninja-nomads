public class temple {
    private String name;
    private String add;
    private String ph;

    public temple() {
        this.name = name;
        this.add = add;
        this.ph = ph;
    }

    public String getName() {
        return name;
    }

    public String getAdd() {
        return add;
    }

    public String getPh() {
        return ph;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAdd(String add) {
        this.add = add;
    }

    public void setPh(String ph) {
        this.ph = ph;
    }
}
